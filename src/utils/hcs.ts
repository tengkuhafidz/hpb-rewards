export interface HcsItem {
  name: string;
  tokens: string[];
}

let items: HcsItem[] = [];

const getHcsItems = () => {
  if(items.length > 0){
    return items;
  }

  items = hcsProductNames.map(item => ({
    name: item,
    tokens: item.split(' ')
  }));

  return items;
};

export const isHcsItem = (itemText: string | null) => {
  if (!itemText) {
    return false;
  }
  return getHcsItems().some(item =>
    item.tokens.every(token =>
      itemText.toLowerCase().includes(token.toLowerCase())
    )
  );
};

